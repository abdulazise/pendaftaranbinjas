<h3>Tata Cara Pendaftaran</h3>

<p>1.Calon mendaftar Online Bintara PK TNI AD melalui website penerimaan prajurit TNI yaitu di alamat http://rekrutmen-tni.mil.id sesuai batas waktu yang telah ditentukan. (Bagi calon yang belum memahami cara mendaftar melalui Online dapat langsung datang ke tempat pendaftaran untuk mendapatkan penjelasan dari petugas pendaftaran bagaimana cara mendaftar dengan membawa persyaratan administrasi sesuai ketentuan yang berlaku).</p>
<p>2.Cetak Printout formulir pendaftaran</p>
<p>3.Datang ke Ajendam/Rem terdekat untuk melaksanakan daftar ulang (di luar tanggal yang telah ditentukan adalah tidak sah).</p>
<p>4.Persiapkan diri sebaik-baiknya untuk mengikuti kegiatan seleksi.</p>
<p>5.Ikuti tahapan seleksi yang telah diatur oleh Panda masing-masing.</p>
<p>6.Selama proses kegiatan penerimaan tidak dipungut biaya apapun.</p>

<h3>Persyaratan umum adalah</h3>

<p>1.Warga Negara Indonesia;</p>
<p>2.Beriman dan bertaqwa kepada Tuhan Yang Maha Esa (menganut salah satu dari 6 agama yang diakui di Indonesia atau penghayat kepercayaan).</p>
<p>3.Setia kepada Negara Kesatuan Republik Indonesia yang berdasarkan Pancasila dan Undang-Undang Dasar Negara Republik Indonesia tahun 1945.</p>
<p>4.Berumur paling rendah 17 tahun 9 bulan dan paling tinggi 22 tahun pada saat pembukaan pendidikan pertama.</p>
<p>5.Tidak memiliki catatan kriminalitas yang dikeluarkan secara tertulis oleh Kepolisian Republik Indonesia.</p>
<p>6.Sehat jasmani dan rohani serta tidak berkacamata; dan</p>
<p>7.Tidak sedang kehilangan hak menjadi prajurit berdasarkan putusan pengadilan yang telah memperoleh kekuatan hukum tetap.</p>


<h3>Berikut persyaratan lain adalah:</h3>

<p>Pria/Wanita, bukan anggota/mantan prajurit TNI/Polri atau PNS TNI.</p>
<p>Berijazah minimal SMA/MA/SMK baik negeri atau swasta yang terakreditasi sesuai kebutuhan (Berlaku Paket C), dengan persyaratan nilai rata-rata:
Lulusan SMA/MA/SMK tahun 2019, nilai ujian nasional rata-rata minimal 40,5 (untuk reguler dan unggulan wilayah di Pulau Jawa, Pulau Sumatera dan provinsi Bali) dan minimal 38,5 untuk wilayah lainnya;
Lulusan SMA/MA/SMK tahun 2020, nilai minimal rata-rata raport dari 3 mata pelajaran (Bahasa Indonesia, Bahasa Inggris dan Matematika) adalah 68.</p>

<p>Lulusan SMA/MA/SMK tahun 2021, nilai minimal rata-rata raport dari 3 mata pelajaran (Bahasa Indonesia, Bahasa Inggris dan Matematika) adalah 70.</p>
<p>Lulusan SMA/MA/SMK tahun 2022, nilai minimal rata-rata raport dari 3 mata pelajaran (Bahasa Indonesia, Bahasa Inggris dan Matematika) adalah 70.</p>
<p>Lulusan SMA/MA/SMK tahun 2023, persyaratan nilai rata-rata akan ditentukan kemudian.</p>
<p>Lulusan pondok pesantren atau sekolah agama Islam (MI, MTs dan MA) khusus untuk Caba PK TNI AD keagamaan (khusus santri/agama Islam).</p>
<p>Belum pernah menikah dan sanggup tidak menikah selama dalam pendidikan pertama sampai dengan 2 (dua) tahun setelah selesai Dikma.</p>
<p>Berumur sekurang-kurangnya 17 tahun 9 bulan dan setinggi-tingginya 22 tahun pada saat pembukaan pendidikan pertama.</p>
<p>Memiliki tinggi badan sekurang-kurangnya 163 cm bagi pria dan 157 cm bagi wanita serta memiliki berat badan seimbang menurut ketentuan yang berlaku.</p>
<p>Bersedia menjalani Ikatan Dinas Pertama (IDP) minimal selama 10 (sepuluh) tahun.</p>
<p>Bersedia ditempatkan di seluruh wilayah Negara Kesatuan Republik Indonesia.</p>
<p>Harus mengikuti pemeriksaan/pengujian yang diselenggarakan oleh panitia penerimaan yang meliputi:
<p>Administrasi, Kesehatan, Jasmani, Litpers, Psikologi; dan Keahlian (khusus keagamaan dan keahlian pria).</p>
