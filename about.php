<h3>Tentang Polri dan TNI</h3>

<p>Polri adalah Kepolisian Nasional di Indonesia, yang bertanggung jawab langsung di bawah Presiden. Polri mempunyai moto Rastra Sewakotama yang artinya Abdi Utama bagi Nusa Bangsa. Polri mengemban tugas-tugas kepolisian di seluruh wilayah Indonesia yaitu memelihara keamanan dan ketertiban masyarakat; menegakkan hukum; dan memberikan perlindungan, pengayoman, dan pelayanan kepada masyarakat.
Tentara Nasional Indonesia atau TNI yang merupakan lembaga militer dibawah departemen pertahanan dan juga dibawah Presiden, terbagi atas 3 jenis angkatan, yakni TNI Angkatan Laut, Angkatan Darat, dan Angkatan Udara. Ketiga jenis angkatan TNI tersebut tentu memiliki tugas dan wewenang yang berbeda berdasarkan cakupan wilayah kerjanya.</p>

<h3>Jasmani / Fisik untuk Polri dan TNI</h3>

<p>Mungkin kita akrab dengan istilah “Di Dalam Tubuh Yang Sehat Terdapat Jiwa Yang Kuat”. Istilah yang sangat populer karya Demicus Iunius Jevenalis dari Romawi. Seorang praktisi olahraga John Hulley kemudian mengangkat istilah ini menjadi motto gerakan hidup sehat di masyarakat Inggris sekitar tahun 1861.

Gerakannya membuahkan hasil. Sekolah-sekolah berasrama di Inggris yang masih didominasi anak orang kaya mulai mendapatkan pelatihan fisik secara menyuluruh, di samping menerima pendidikan pengetahuan dasar. Slogan ini kemudian menyebar menjadi sebuah slogan hidup untuk mengajak orang menjaga dan meningkatkan kemampuan fisik melalui olahraga.

Dalam Tes Polri dan TNI, ini merupakan bagian yang wajib dalam proses rekrutmen. Banayk yang menyebutnya tes Jasmani / Fisik. Dengan kata lain, tes ini merupakan proses seleksi yang berkaitan dengan fisik. Walaupun secara spesifik memiliki standarisasi yang berbeda namun secara umum mereka memiliki teknik yang sama.</p>

<h3>Bimbel Polri dan TNI Kami</h3>

<p>Terkait dengan TNI, seperti yang kita ketahui profesi ini adalah profesi eksklusif dimana banyak orang yang mengimpikan dirinya untuk berseragam institusi militer tersebut. Kesan atas pembawaan yang gagah dan prospek masa depan dunia kerja yang terjamin itu menjadi alasan banyak orang untuk mengidam-idamkan posisi sebagai anggota TNI.

Jika Anda adalah salah satu diantara orang-orang yang ingin masuk TNI, Anda harus mempersiapkan banyak hal selain hanya niat saja. Serangkaian tes yang akan Anda hadapi ketika proses penerimaan TNI nanti adalah proses bertahap yang sangat ketat persaingan dan membutuhkan kesiapan fisik dan mental.

Siapkah Anda untuk berkompetisi dengan calon anggota lainnya? Sudahkan Anda mempersiapkan sedini mungkin apa yang dibutuhkan nantinya? atau justru Anda belum mengetahui apa saja yang harus dipersiapkan?

Anda tak perlu khawatir untuk mondar-mandir mencari joki atau jasa yang dianggap kompeten membantu persiapan Anda. Karena Anda sudah menemukan jawabannya di sini. Kami, Panara Course siap menyediakan Bimbel Jasmani / Fisik Polri & TNI untuk Anda yang berniat mengikuti seleksi Polri dan TNI tahun ini.

